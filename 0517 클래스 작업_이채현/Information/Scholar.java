package Information;

public class Scholar {
	//attributes
	public String name;
	public double std_score;
	public int std_grade;
	
	
	//methods
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getstd_score() {
		return std_score;
	}
	public void setstd_score(double std_score) {
		this.std_score = std_score;
	}
	public int getStd_grade() {
		return std_grade;
	}
	public void setStd_grade(int std_grade) {
		this.std_grade = std_grade;
	}
	
	

}